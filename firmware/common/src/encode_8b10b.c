void encode_8b10b_init(encode_8b10b_state_t *state)
{
    state->rd = 0;  /* Start with RD- */
}

uint16_t encode_8b10b_byte(encode_8b10b_state_t *state, uint8_t byte)
{
    uint16_t entry = enc_8b10b_lut[state->rd][byte];
    state->rd ^= (entry >> 10) & 1;
    return entry & 0x3FF;
}

uint16_t encode_8b10b_k28_5(encode_8b10b_state_t *state)
{
    static const uint16_t k28_5[2] = { 0x17C, 0x283 };
    uint16_t sym = k28_5[state->rd];
    state->rd ^= 1;  /* K28.5 always flips RD */
    return sym;
}

void encode_8b10b_buffer(encode_8b10b_state_t *state,
                         const uint8_t *input,
                         uint16_t *output,
                         uint32_t count)
{
    uint8_t rd = state->rd;
    for (uint32_t i = 0; i < count; i++) {
        uint16_t entry = enc_8b10b_lut[rd][input[i]];
        output[i] = entry & 0x3FF;
        rd ^= (entry >> 10) & 1;
    }
    state->rd = rd;
}

void encode_8b10b_pack_10to32(const uint16_t *symbols,
                               uint32_t *packed,
                               uint32_t symbol_count)
{
    uint32_t accum = 0;
    uint32_t bits = 0;
    uint32_t out_idx = 0;

    for (uint32_t i = 0; i < symbol_count; i++) {
        accum |= (uint32_t)(symbols[i] & 0x3FF) << bits;
        bits += 10;
        if (bits >= 32) {
            packed[out_idx++] = accum;
            bits -= 32;
            accum = (bits > 0)
                  ? ((uint32_t)(symbols[i] & 0x3FF) >> (10 - bits))
                  : 0;
        }
    }
    if (bits > 0) {
        packed[out_idx] = accum;
    }
}

void encode_8b10b_encode_and_pack_16(encode_8b10b_state_t *state,
                                      const uint8_t input[16],
                                      uint32_t packed[5])
{
    uint16_t sym[16];
    uint8_t rd = state->rd;

    for (int i = 0; i < 16; i++) {
        uint16_t entry = enc_8b10b_lut[rd][input[i]];
        sym[i] = entry & 0x3FF;
        rd ^= (entry >> 10) & 1;
    }
    state->rd = rd;

    /* Pack 16 x 10-bit symbols into 5 x 32-bit words (160 bits) */
    packed[0] = (uint32_t)sym[0]
              | ((uint32_t)sym[1]  << 10)
              | ((uint32_t)sym[2]  << 20)
              | ((uint32_t)sym[3]  << 30);
    packed[1] = ((uint32_t)sym[3]  >> 2)
              | ((uint32_t)sym[4]  << 8)
              | ((uint32_t)sym[5]  << 18)
              | ((uint32_t)sym[6]  << 28);
    packed[2] = ((uint32_t)sym[6]  >> 4)
              | ((uint32_t)sym[7]  << 6)
              | ((uint32_t)sym[8]  << 16)
              | ((uint32_t)sym[9]  << 26);
    packed[3] = ((uint32_t)sym[9]  >> 6)
              | ((uint32_t)sym[10] << 4)
              | ((uint32_t)sym[11] << 14)
              | ((uint32_t)sym[12] << 24);
    packed[4] = ((uint32_t)sym[12] >> 8)
              | ((uint32_t)sym[13] << 2)
              | ((uint32_t)sym[14] << 12)
              | ((uint32_t)sym[15] << 22);
}
